        glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glDepthMask(GL_TRUE);

        // don't forget to enable shader before setting uniforms
        ourShader.use();
        // view/projection transformations
        
        ourShader.setMat4("projection", projection);
        ourShader.setMat4("view", view);

        // render the loaded model
        glm::mat4 model = glm::mat4(1.0f);
        model = glm::translate(model, glm::vec3(0.0f, 0.0f, 0.0f)); // translate it down so it's at the center of the scene
        model = glm::scale(model, glm::vec3(1.0f, 1.0f, 1.0f));	// it's a bit too big for our scene, so scale it down
        glm::transpose(glm::inverse(model));
        ourShader.setVec3("dirLight.direction",glm::vec3(3.0f,1.0f,2.0f));
        ourShader.setVec3("cameraPos",camera.Position);
        ourShader.setInt("skybox",0);
        ourShader.setVec3("dirLight.ambient",0.1f*lightColor);
        ourShader.setVec3("dirLight.diffuse",0.5f*lightColor);
        ourShader.setVec3("dirLight.specular",lightColor);
        ourShader.setMat3("faxianjuzhen",model);
        ourShader.setVec3("viewPos",camera.Position);
